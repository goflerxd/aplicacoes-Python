# programa para calcular a área do losango
# area = (diagonal maior * diagonal menor)  /2

print('Calcular a área do losango')

dmaior = float(input('Digite a Diagonal maior:'  ))
dmenor = float(input('Digite a diagonal menor:'  ))


area = dmaior * dmenor  / 2 

print('Área do losango é:' ,round(area,2),".")